# Class: A blueprint for creating objects with attributes and methods
class Human():
    # Attributes: Characteristics of the class with default values
    eye  = 'Blue'
    hair = 'Brown'
    Age  = 26

    # Methods: Functions defining the behavior of the class
    def walk(Self):
        return "I am walking"
    
    def talk(Self):
        return "I am talking"

# Object: An instance of the class
Raj = Human()
